struct GraphResources {
  std::unique_ptr<struct ggml_context, decltype(&ggml_free)> weightsCtx{
      nullptr, ggml_free};
  std::unique_ptr<struct ggml_context, decltype(&ggml_free)> graphCtx{
      nullptr, ggml_free};
  std::unordered_map<std::string, struct ggml_tensor*> weights;
  ggml_backend_t backend = nullptr;
  ggml_backend_buffer_t weightsBuffer = nullptr;
  ggml_backend_buffer_t graphBuffer = nullptr;
  struct ggml_cgraph* graph = nullptr;
  struct ggml_tensor* input = nullptr;
  struct ggml_tensor* features = nullptr;

  // LSTM + linear classifier weights uploaded to the backend so the recurrent
  // tail can run on the GPU (batched ggml matmuls) instead of scalar CPU.
  std::unique_ptr<struct ggml_context, decltype(&ggml_free)> lstmCtx{
      nullptr, ggml_free};
  ggml_backend_buffer_t lstmBuffer = nullptr;
  std::unordered_map<std::string, struct ggml_tensor*> lstmWeights;

  GraphResources() = default;
  ~GraphResources() { reset(); }
  GraphResources(const GraphResources&) = delete;
  GraphResources& operator=(const GraphResources&) = delete;
  GraphResources(GraphResources&&) = delete;
  GraphResources& operator=(GraphResources&&) = delete;

  void reset() {
    graph = nullptr;
    input = nullptr;
    features = nullptr;
    graphCtx.reset();
    if (graphBuffer != nullptr) {
      ggml_backend_buffer_free(graphBuffer);
      graphBuffer = nullptr;
    }
    weights.clear();
    weightsCtx.reset();
    if (weightsBuffer != nullptr) {
      ggml_backend_buffer_free(weightsBuffer);
      weightsBuffer = nullptr;
    }
    lstmWeights.clear();
    lstmCtx.reset();
    if (lstmBuffer != nullptr) {
      ggml_backend_buffer_free(lstmBuffer);
      lstmBuffer = nullptr;
    }
    if (backend != nullptr) {
      ggml_backend_free(backend);
      backend = nullptr;
    }
  }
};

struct GraphBuilder {
  struct ggml_context* ctx;
  // GraphBuilder is a stateless one-shot helper that never outlives its
  // caller; storing the weight map by reference avoids a deep copy on every
  // graph build.
  // NOLINTNEXTLINE(cppcoreguidelines-avoid-const-or-ref-data-members)
  const std::unordered_map<std::string, struct ggml_tensor*>& w;

  [[nodiscard]] struct ggml_tensor* t(const std::string& name) const {
    auto it = w.find(name);
    if (it == w.end()) {
      raise("missing graph tensor: " + name);
    }
    return it->second;
  }

  struct ggml_tensor* activate(struct ggml_tensor* x, bool useHardswish) const {
    return useHardswish ? ggml_hardswish(ctx, x) : ggml_relu(ctx, x);
  }

  struct ggml_tensor*
  applyBn(struct ggml_tensor* x, const std::string& bnPrefix) const {
    // The BN per-channel scale is folded into the preceding conv's weights at
    // load time (see the ".scale" loop in load()), so only the shift remains.
    // This drops one full-tensor multiply per conv from the feature-extractor
    // graph, which runs once per recognition batch (many times per page).
    return ggml_add(ctx, x, t(bnPrefix + ".shift"));
  }

  struct ggml_tensor* convBnAct(
      struct ggml_tensor* x,
      // NOLINTNEXTLINE(bugprone-easily-swappable-parameters)
      const std::string& convPrefix, const std::string& bnPrefix, int strideW,
      int strideH, int kernel, bool applyActivation, bool useHardswish) const {
    struct ggml_tensor* conv = ggml_conv_2d(
        ctx,
        t(convPrefix + ".weight"),
        x,
        strideW,
        strideH,
        samePadding(kernel),
        samePadding(kernel),
        1,
        1);
    conv = applyBn(conv, bnPrefix);
    return applyActivation ? activate(conv, useHardswish) : conv;
  }

  struct ggml_tensor* dwConvBnAct(
      struct ggml_tensor* x,
      // NOLINTNEXTLINE(bugprone-easily-swappable-parameters)
      const std::string& convPrefix, const std::string& bnPrefix, int strideW,
      int strideH, int kernel, bool useHardswish) const {
    struct ggml_tensor* conv = ggml_conv_2d_dw(
        ctx,
        t(convPrefix + ".weight"),
        x,
        strideW,
        strideH,
        samePadding(kernel),
        samePadding(kernel),
        1,
        1);
    conv = applyBn(conv, bnPrefix);
    return activate(conv, useHardswish);
  }

  struct ggml_tensor* convBiasAct(
      struct ggml_tensor* x, const std::string& convPrefix,
      bool applyActivation, bool useHardswish) const {
    struct ggml_tensor* conv =
        ggml_conv_2d(ctx, t(convPrefix + ".weight"), x, 1, 1, 0, 0, 1, 1);
    conv = ggml_add(ctx, conv, t(convPrefix + ".bias_br"));
    return applyActivation ? activate(conv, useHardswish) : conv;
  }

  struct ggml_tensor*
  seBlock(struct ggml_tensor* x, const std::string& sePrefix) const {
    struct ggml_tensor* pooled = ggml_pool_2d(
        ctx,
        x,
        GGML_OP_POOL_AVG,
        static_cast<int>(x->ne[0]),
        static_cast<int>(x->ne[1]),
        static_cast<int>(x->ne[0]),
        static_cast<int>(x->ne[1]),
        0,
        0);
    struct ggml_tensor* fc1 =
        convBiasAct(pooled, sePrefix + ".fc1", true, false);
    struct ggml_tensor* fc2 = convBiasAct(fc1, sePrefix + ".fc2", false, false);
    return ggml_mul(ctx, x, ggml_hardsigmoid(ctx, fc2));
  }

  struct ggml_tensor*
  invertedResidual(struct ggml_tensor* x, const BlockConfig& cfg) const {
    const std::string base =
        "crnn.features." + std::to_string(cfg.featureIndex);
    const bool hasExpand = cfg.expansionChannels != cfg.inputChannels;
    int dwBlockIdx = 0;
    int seBlockIdx = -1;
    int projBlockIdx = 0;

    struct ggml_tensor* y = x;
    if (hasExpand) {
      y = convBnAct(
          y,
          base + ".block.0.0",
          base + ".block.0.1",
          1,
          1,
          1,
          true,
          cfg.useHardswish);
      dwBlockIdx = 1;
      if (cfg.useSe) {
        seBlockIdx = 2;
        projBlockIdx = 3;
      } else {
        projBlockIdx = 2;
      }
    } else if (cfg.useSe) {
      seBlockIdx = 1;
      projBlockIdx = 2;
    } else {
      projBlockIdx = 1;
    }

    const std::string dwBase = base + ".block." + std::to_string(dwBlockIdx);
    y = dwConvBnAct(
        y,
        dwBase + ".0",
        dwBase + ".1",
        cfg.strideW,
        cfg.strideH,
        cfg.kernel,
        cfg.useHardswish);

    if (cfg.useSe) {
      y = seBlock(y, base + ".block." + std::to_string(seBlockIdx));
    }

    const std::string projBase =
        base + ".block." + std::to_string(projBlockIdx);
    y = convBnAct(
        y, projBase + ".0", projBase + ".1", 1, 1, 1, false, cfg.useHardswish);

    if (cfg.strideW == 1 && cfg.strideH == 1 &&
        cfg.inputChannels == cfg.outputChannels) {
      y = ggml_add(ctx, y, x);
    }
    return y;
  }
};

