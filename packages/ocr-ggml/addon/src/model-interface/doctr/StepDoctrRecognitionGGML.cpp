struct StepDoctrRecognitionGGML::Impl {
  GraphResources graph;
  std::array<std::array<LstmWeights, kLstmDirectionCount>, kLstmLayerCount>
      lstm{};
  std::vector<float> linearWeight;
  std::vector<float> linearBias;
  // Number of crops the feature-extractor graph processes per compute call.
  int batchSize = 1;

  explicit Impl(
      const std::string& pathRecognizer, ggml_backend_dev_t backendDevice,
      int batchSizeArg)
      : batchSize(batchSizeArg > 0 ? batchSizeArg : 1) {
    load(pathRecognizer, backendDevice);
  }

  void
  load(const std::string& pathRecognizer, ggml_backend_dev_t backendDevice) {
    graph.reset();
    ggml_backend_dev_t dev =
        (backendDevice != nullptr)
            ? backendDevice
            : ggml_backend_dev_by_type(GGML_BACKEND_DEVICE_TYPE_CPU);
    // The MobileNetV3 feature-extractor graph and the bidirectional LSTM +
    // linear classifier (see runLstmLinearGpu) both run on this ggml backend.
    // A scalar CPU implementation of the recurrent tail is retained as a
    // fallback (selectable via OCR_DOCTR_LSTM_CPU=1).
    graph.backend = dev ? ggml_backend_dev_init(dev, nullptr) : nullptr;
    if (graph.backend == nullptr) {
      raise("failed to initialize ggml backend");
    }

    struct ggml_context* ggufGgmlCtx = nullptr;
    gguf_init_params params{.no_alloc = false, .ctx = &ggufGgmlCtx};
    gguf_context* gguf = gguf_init_from_file(pathRecognizer.c_str(), params);
    if (gguf == nullptr) {
      raise("failed to open GGUF file: " + pathRecognizer);
    }
    std::unique_ptr<gguf_context, decltype(&gguf_free)> ggufGuard(
        gguf, gguf_free);
    std::unique_ptr<struct ggml_context, decltype(&ggml_free)> srcCtx(
        ggufGgmlCtx, ggml_free);

    loadGraphWeights(srcCtx.get());
    loadRecurrentWeights(srcCtx.get());
    uploadLstmWeightsToBackend();
    buildGraph();
  }

  // Upload the LSTM gate weights and the final linear classifier to the ggml
  // backend so the recurrent tail runs on the GPU. Layout note: a PyTorch
  // weight stored row-major as [rows, cols] maps to a ggml tensor ne=[cols,
  // rows] with identical memory, which is exactly the `a` operand ggml_mul_mat
  // wants (result[m] = sum_k a[k,m] b[k]). So each vector uploads verbatim.
  void uploadLstmWeightsToBackend() {
    graph.lstmCtx = std::unique_ptr<struct ggml_context, decltype(&ggml_free)>(
        ggml_init(
            {.mem_size = ggml_tensor_overhead() * 128,
             .mem_buffer = nullptr,
             .no_alloc = true}),
        ggml_free);
    if (!graph.lstmCtx) {
      raise("failed to allocate LSTM weights context");
    }
    struct ggml_context* ctx = graph.lstmCtx.get();
    constexpr int gates = kLstmGateCount * kLstmHiddenSize; // 4H

    auto mk2d = [&](const std::string& name, int64_t ne0, int64_t ne1) {
      struct ggml_tensor* tns =
          ggml_new_tensor_2d(ctx, GGML_TYPE_F32, ne0, ne1);
      ggml_set_name(tns, name.c_str());
      graph.lstmWeights.emplace(name, tns);
      return tns;
    };
    auto mk1d = [&](const std::string& name, int64_t ne0) {
      struct ggml_tensor* tns = ggml_new_tensor_1d(ctx, GGML_TYPE_F32, ne0);
      ggml_set_name(tns, name.c_str());
      graph.lstmWeights.emplace(name, tns);
      return tns;
    };

    for (int layer = 0; layer < kLstmLayerCount; ++layer) {
      const int inSize = (layer == 0) ? kFeatureChannels
                                      : kLstmHiddenSize * kLstmDirectionCount;
      for (int dir = 0; dir < kLstmDirectionCount; ++dir) {
        const std::string sfx =
            "_l" + std::to_string(layer) + "_" + std::to_string(dir);
        mk2d("wih" + sfx, inSize, gates);
        mk2d("whh" + sfx, kLstmHiddenSize, gates);
        mk1d("bias" + sfx, gates);
      }
    }
    mk2d("wlin", kLstmHiddenSize * kLstmDirectionCount, kVocabSize);
    mk1d("blin", kVocabSize);

    graph.lstmBuffer = ggml_backend_alloc_ctx_tensors(ctx, graph.backend);
    if (graph.lstmBuffer == nullptr) {
      raise("failed to allocate LSTM weights buffer");
    }
    ggml_backend_buffer_set_usage(
        graph.lstmBuffer, GGML_BACKEND_BUFFER_USAGE_WEIGHTS);

    auto set = [&](const std::string& name, const std::vector<float>& v) {
      struct ggml_tensor* tns = graph.lstmWeights.at(name);
      if (static_cast<size_t>(ggml_nelements(tns)) != v.size()) {
        raise("LSTM weight size mismatch for " + name);
      }
      ggml_backend_tensor_set(tns, v.data(), 0, v.size() * sizeof(float));
    };

    for (int layer = 0; layer < kLstmLayerCount; ++layer) {
      for (int dir = 0; dir < kLstmDirectionCount; ++dir) {
        const std::string sfx =
            "_l" + std::to_string(layer) + "_" + std::to_string(dir);
        const LstmWeights& w =
            lstm[static_cast<size_t>(layer)][static_cast<size_t>(dir)];
        set("wih" + sfx, w.weightIh);
        set("whh" + sfx, w.weightHh);
        // ggml needs a single combined gate bias; PyTorch keeps ih + hh apart.
        std::vector<float> bias(static_cast<size_t>(gates));
        for (int g = 0; g < gates; ++g) {
          bias[static_cast<size_t>(g)] = w.biasIh[static_cast<size_t>(g)] +
                                         w.biasHh[static_cast<size_t>(g)];
        }
        set("bias" + sfx, bias);
      }
    }
    set("wlin", linearWeight);
    set("blin", linearBias);
  }

  // Builds and runs a batched bidirectional 2-layer LSTM + linear classifier on
  // the backend for all `n` crops at once, returning logits laid out
  // [vocab, seq, n] (per crop: [seq, vocab] vocab-fastest, matching decodeCTC).
  // `allFeatures` is the concatenated feature-extractor output, per crop
  // [seq, channels] seq-fastest (featStride = seq * kFeatureChannels).
  [[nodiscard]] std::vector<float>
  runLstmLinearGpu(const std::vector<float>& allFeatures, int n) const {
    constexpr int H = kLstmHiddenSize;
    constexpr int gates = kLstmGateCount * H;
    constexpr int seq = kSequenceLength;

    const size_t graphMem = (ggml_tensor_overhead() * 6144) +
                            ggml_graph_overhead_custom(8192, false);
    std::unique_ptr<struct ggml_context, decltype(&ggml_free)> gctx(
        ggml_init(
            {.mem_size = graphMem, .mem_buffer = nullptr, .no_alloc = true}),
        ggml_free);
    if (!gctx) {
      raise("failed to allocate LSTM graph context");
    }
    struct ggml_context* ctx = gctx.get();
    const auto W = [&](const std::string& name) {
      return graph.lstmWeights.at(name);
    };

    // Feature input: [seq, channels, n] (matches allFeatures memory), then
    // transpose to X = [channels, seq, n] for the gate matmul.
    struct ggml_tensor* feat =
        ggml_new_tensor_3d(ctx, GGML_TYPE_F32, seq, kFeatureChannels, n);
    ggml_set_name(feat, "lstm_feat_in");
    ggml_set_input(feat);
    struct ggml_tensor* x = ggml_cont(
        ctx, ggml_permute(ctx, feat, 1, 0, 2, 3)); // [channels, seq, n]

    for (int layer = 0; layer < kLstmLayerCount; ++layer) {
      struct ggml_tensor* dirOut[kLstmDirectionCount] = {nullptr, nullptr};
      for (int dir = 0; dir < kLstmDirectionCount; ++dir) {
        const std::string sfx =
            "_l" + std::to_string(layer) + "_" + std::to_string(dir);
        // Z = Wih . X for every timestep at once: [4H, seq, n].
        struct ggml_tensor* z = ggml_mul_mat(ctx, W("wih" + sfx), x);
        z = ggml_add(ctx, z, W("bias" + sfx));

        // Initial hidden/cell state is zero. Rather than allocate a zero
        // tensor (ggml_scale of uninitialised gallocr memory is 0*NaN==NaN on
        // some hosts), the first timestep is special-cased: the W_hh . h_prev
        // term vanishes and C = i .* g_cell.
        struct ggml_tensor* hPrev = nullptr;
        struct ggml_tensor* cPrev = nullptr;
        std::vector<struct ggml_tensor*> hSteps(static_cast<size_t>(seq));

        for (int step = 0; step < seq; ++step) {
          const int t = (dir == 0) ? step : (seq - 1 - step);
          struct ggml_tensor* zt = ggml_cont(
              ctx,
              ggml_view_2d(
                  ctx,
                  z,
                  gates,
                  n,
                  z->nb[2],
                  static_cast<size_t>(t) * z->nb[1])); // [4H, n]
          struct ggml_tensor* g =
              (hPrev == nullptr)
                  ? zt
                  : ggml_add(ctx, zt, ggml_mul_mat(ctx, W("whh" + sfx), hPrev));

          const auto gate = [&](int k) {
            return ggml_cont(
                ctx,
                ggml_view_2d(
                    ctx,
                    g,
                    H,
                    n,
                    g->nb[1],
                    static_cast<size_t>(k) * H * sizeof(float)));
          };
          struct ggml_tensor* ig = ggml_sigmoid(ctx, gate(0));
          struct ggml_tensor* fg = ggml_sigmoid(ctx, gate(1));
          struct ggml_tensor* cg = ggml_tanh(ctx, gate(2));
          struct ggml_tensor* og = ggml_sigmoid(ctx, gate(3));

          struct ggml_tensor* cCur =
              (cPrev == nullptr)
                  ? ggml_mul(ctx, ig, cg)
                  : ggml_add(
                        ctx, ggml_mul(ctx, fg, cPrev), ggml_mul(ctx, ig, cg));
          struct ggml_tensor* hCur = ggml_mul(ctx, og, ggml_tanh(ctx, cCur));
          hSteps[static_cast<size_t>(t)] = hCur;
          hPrev = hCur;
          cPrev = cCur;
        }

        // Assemble [H, seq, n] from the per-step hidden states. A sequential
        // concat copies the growing tensor each step (O(seq^2) data); a
        // balanced pairwise tree copies the full data once per level
        // (O(seq log seq)) — ggml_concat is a relatively costly Metal copy.
        std::vector<struct ggml_tensor*> level(static_cast<size_t>(seq));
        for (int t = 0; t < seq; ++t) {
          level[static_cast<size_t>(t)] =
              ggml_reshape_3d(ctx, hSteps[static_cast<size_t>(t)], H, 1, n);
        }
        while (level.size() > 1) {
          std::vector<struct ggml_tensor*> next;
          next.reserve((level.size() + 1) / 2);
          for (size_t i = 0; i < level.size(); i += 2) {
            if (i + 1 < level.size()) {
              next.push_back(ggml_concat(ctx, level[i], level[i + 1], 1));
            } else {
              next.push_back(level[i]);
            }
          }
          level.swap(next);
        }
        dirOut[dir] = level[0]; // [H, seq, n]
      }
      // Concat directions along the feature axis -> [2H, seq, n].
      x = ggml_concat(ctx, dirOut[0], dirOut[1], 0);
    }

    // Linear classifier: [vocab, seq, n].
    struct ggml_tensor* logits = ggml_mul_mat(ctx, W("wlin"), x);
    logits = ggml_add(ctx, logits, W("blin"));
    ggml_set_output(logits);

    struct ggml_cgraph* cg = ggml_new_graph_custom(ctx, 8192, false);
    ggml_build_forward_expand(cg, logits);

    ggml_gallocr_t alloc =
        ggml_gallocr_new(ggml_backend_get_default_buffer_type(graph.backend));
    if (alloc == nullptr) {
      raise("failed to allocate LSTM gallocr");
    }
    if (!ggml_gallocr_alloc_graph(alloc, cg)) {
      ggml_gallocr_free(alloc);
      raise("failed to allocate LSTM graph");
    }

    ggml_backend_tensor_set(
        feat,
        allFeatures.data(),
        0,
        static_cast<size_t>(n) * seq * kFeatureChannels * sizeof(float));

    const ggml_status status = ggml_backend_graph_compute(graph.backend, cg);
    if (status != GGML_STATUS_SUCCESS) {
      ggml_gallocr_free(alloc);
      raise(
          "LSTM graph compute failed: " +
          std::to_string(static_cast<int>(status)));
    }

    std::vector<float> out(static_cast<size_t>(ggml_nelements(logits)));
    ggml_backend_tensor_get(logits, out.data(), 0, out.size() * sizeof(float));
    ggml_gallocr_free(alloc);
    return out;
  }

  [[nodiscard]] std::vector<float>
  runFeatureExtractor(const std::vector<float>& inputWhcn) const {
    ggml_backend_tensor_set(
        graph.input, inputWhcn.data(), 0, inputWhcn.size() * sizeof(float));

    const ggml_status status =
        ggml_backend_graph_compute(graph.backend, graph.graph);
    if (status != GGML_STATUS_SUCCESS) {
      raise(
          "ggml backend graph compute failed with status " +
          std::to_string(static_cast<int>(status)));
    }

    std::vector<float> features(
        static_cast<size_t>(ggml_nelements(graph.features)));
    ggml_backend_tensor_get(
        graph.features, features.data(), 0, features.size() * sizeof(float));
    return features;
  }

  [[nodiscard]] std::vector<float>
  runLstmLinear(const float* featureWhcn) const {
    std::vector<float> layerInput(
        static_cast<size_t>(kSequenceLength) * kFeatureChannels);
    for (int t = 0; t < kSequenceLength; ++t) {
      for (int c = 0; c < kFeatureChannels; ++c) {
        layerInput[(static_cast<size_t>(t) * kFeatureChannels) + c] =
            featureWhcn[t + (static_cast<size_t>(kSequenceLength) * c)];
      }
    }

    std::vector<float> layerOutput;
    for (int layer = 0; layer < kLstmLayerCount; ++layer) {
      const int inputSize = (layer == 0)
                                ? kFeatureChannels
                                : kLstmHiddenSize * kLstmDirectionCount;
      layerOutput.assign(
          static_cast<size_t>(kSequenceLength) * kLstmHiddenSize *
              kLstmDirectionCount,
          0.0F);
      runLstmDirection(
          layerInput, layerOutput, lstm[layer][0], inputSize, false);
      runLstmDirection(
          layerInput, layerOutput, lstm[layer][1], inputSize, true);
      layerInput.swap(layerOutput);
    }

    std::vector<float> logits(
        static_cast<size_t>(kSequenceLength) * kVocabSize);
    constexpr int finalFeatureSize = kLstmHiddenSize * kLstmDirectionCount;
    for (int t = 0; t < kSequenceLength; ++t) {
      const float* x =
          layerInput.data() + (static_cast<size_t>(t) * finalFeatureSize);
      for (int cls = 0; cls < kVocabSize; ++cls) {
        float value = linearBias[static_cast<size_t>(cls)];
        const float* w =
            linearWeight.data() + (static_cast<size_t>(cls) * finalFeatureSize);
        for (int i = 0; i < finalFeatureSize; ++i) {
          value += w[i] * x[i];
        }
        logits[(static_cast<size_t>(t) * kVocabSize) + cls] = value;
      }
    }
    return logits;
  }

private:
  static void runLstmDirection(
      const std::vector<float>& input, std::vector<float>& output,
      const LstmWeights& weights, int inputSize, bool reverse) {
    std::vector<float> hidden(kLstmHiddenSize, 0.0F);
    std::vector<float> cell(kLstmHiddenSize, 0.0F);
    std::array<float, static_cast<size_t>(kLstmGateCount) * kLstmHiddenSize>
        gates{};

    for (int step = 0; step < kSequenceLength; ++step) {
      const int t = reverse ? (kSequenceLength - 1 - step) : step;
      const float* x = input.data() + (static_cast<size_t>(t) * inputSize);

      for (int gate = 0; gate < kLstmGateCount * kLstmHiddenSize; ++gate) {
        float value = weights.biasIh[static_cast<size_t>(gate)] +
                      weights.biasHh[static_cast<size_t>(gate)];
        const float* wIh =
            weights.weightIh.data() + (static_cast<size_t>(gate) * inputSize);
        for (int i = 0; i < inputSize; ++i) {
          value += wIh[i] * x[i];
        }
        const float* wHh = weights.weightHh.data() +
                           (static_cast<size_t>(gate) * kLstmHiddenSize);
        for (int i = 0; i < kLstmHiddenSize; ++i) {
          value += wHh[i] * hidden[static_cast<size_t>(i)];
        }
        gates[static_cast<size_t>(gate)] = value;
      }

      for (int i = 0; i < kLstmHiddenSize; ++i) {
        const float inputGate = sigmoid(gates[static_cast<size_t>(i)]);
        const float forgetGate =
            sigmoid(gates[kLstmHiddenSize + static_cast<size_t>(i)]);
        const float cellGate =
            std::tanh(gates[(static_cast<size_t>(2) * kLstmHiddenSize) + i]);
        const float outputGate =
            sigmoid(gates[(static_cast<size_t>(3) * kLstmHiddenSize) + i]);
        cell[static_cast<size_t>(i)] =
            (forgetGate * cell[static_cast<size_t>(i)]) +
            (inputGate * cellGate);
        hidden[static_cast<size_t>(i)] =
            outputGate * std::tanh(cell[static_cast<size_t>(i)]);
      }

      const int directionOffset = reverse ? kLstmHiddenSize : 0;
      float* out =
          output.data() +
          static_cast<size_t>(
              (t * kLstmHiddenSize * kLstmDirectionCount) + directionOffset);
      std::memcpy(out, hidden.data(), kLstmHiddenSize * sizeof(float));
    }
  }

  // NOLINTNEXTLINE(readability-function-cognitive-complexity)
  void loadGraphWeights(struct ggml_context* srcCtx) {
    graph.weightsCtx =
        std::unique_ptr<struct ggml_context, decltype(&ggml_free)>(
            ggml_init(
                {.mem_size = ggml_tensor_overhead() * kWeightsCtxTensorOverhead,
                 .mem_buffer = nullptr,
                 .no_alloc = true}),
            ggml_free);
    if (!graph.weightsCtx) {
      raise("failed to allocate GGML weights context");
    }

    auto addRaw = [&](const std::string& name) {
      struct ggml_tensor* dst = cloneRaw(graph.weightsCtx.get(), srcCtx, name);
      graph.weights.emplace(name, dst);
      return dst;
    };

    auto addConvWeight = [&](const std::string& name) {
      struct ggml_tensor* src = ggml_get_tensor(srcCtx, name.c_str());
      if (src == nullptr) {
        raise("missing GGUF tensor: " + name);
      }
      struct ggml_tensor* dst =
          newF16TensorLike(graph.weightsCtx.get(), src, name);
      graph.weights.emplace(name, dst);
      return dst;
    };

    auto addBiasBroadcast = [&](const std::string& name) {
      const std::vector<float> values =
          tensorToF32(ggml_get_tensor(srcCtx, name.c_str()), name);
      const std::array<int64_t, 4> shape = {
          1, 1, static_cast<int64_t>(values.size()), 1};
      graph.weights.emplace(
          name + "_br",
          newF32Tensor(graph.weightsCtx.get(), name + "_br", 4, shape.data()));
    };

    auto addBnAffine = [&](const std::string& prefix) {
      const std::vector<float> weight = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".weight").c_str()),
          prefix + ".weight");
      const std::vector<float> bias = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".bias").c_str()),
          prefix + ".bias");
      const std::vector<float> mean = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".running_mean").c_str()),
          prefix + ".running_mean");
      const std::vector<float> var = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".running_var").c_str()),
          prefix + ".running_var");
      if (weight.size() != bias.size() || weight.size() != mean.size() ||
          weight.size() != var.size()) {
        raise("BatchNorm tensor size mismatch for " + prefix);
      }
      const std::array<int64_t, 4> shape = {
          1, 1, static_cast<int64_t>(weight.size()), 1};
      graph.weights.emplace(
          prefix + ".scale",
          newF32Tensor(
              graph.weightsCtx.get(), prefix + ".scale", 4, shape.data()));
      graph.weights.emplace(
          prefix + ".shift",
          newF32Tensor(
              graph.weightsCtx.get(), prefix + ".shift", 4, shape.data()));
    };

    auto addConvBn = [&](const std::string& convPrefix,
                         const std::string& bnPrefix) {
      addConvWeight(convPrefix + ".weight");
      addBnAffine(bnPrefix);
    };

    auto addConvBias = [&](const std::string& convPrefix) {
      addConvWeight(convPrefix + ".weight");
      addRaw(convPrefix + ".bias");
      addBiasBroadcast(convPrefix + ".bias");
    };

    addConvBn("crnn.features.0.0", "crnn.features.0.1");
    for (const BlockConfig& cfg : kBlocks) {
      const std::string base =
          "crnn.features." + std::to_string(cfg.featureIndex);
      const bool hasExpand = cfg.expansionChannels != cfg.inputChannels;
      int dwBlockIdx = 0;
      int seBlockIdx = -1;
      int projBlockIdx = 0;
      if (hasExpand) {
        addConvBn(base + ".block.0.0", base + ".block.0.1");
        dwBlockIdx = 1;
        if (cfg.useSe) {
          seBlockIdx = 2;
          projBlockIdx = 3;
        } else {
          projBlockIdx = 2;
        }
      } else if (cfg.useSe) {
        seBlockIdx = 1;
        projBlockIdx = 2;
      } else {
        projBlockIdx = 1;
      }

      const std::string dwBase = base + ".block." + std::to_string(dwBlockIdx);
      addConvBn(dwBase + ".0", dwBase + ".1");
      if (cfg.useSe) {
        const std::string seBase =
            base + ".block." + std::to_string(seBlockIdx);
        addConvBias(seBase + ".fc1");
        addConvBias(seBase + ".fc2");
      }
      const std::string projBase =
          base + ".block." + std::to_string(projBlockIdx);
      addConvBn(projBase + ".0", projBase + ".1");
    }
    addConvBn("crnn.features.12.0", "crnn.features.12.1");

    graph.weightsBuffer =
        ggml_backend_alloc_ctx_tensors(graph.weightsCtx.get(), graph.backend);
    if (graph.weightsBuffer == nullptr) {
      raise("failed to allocate GGML weight buffer");
    }
    ggml_backend_buffer_set_usage(
        graph.weightsBuffer, GGML_BACKEND_BUFFER_USAGE_WEIGHTS);

    for (const auto& [name, dst] : graph.weights) {
      if (name.ends_with(".scale") || name.ends_with(".shift") ||
          name.ends_with("_br")) {
        continue;
      }
      struct ggml_tensor* src = ggml_get_tensor(srcCtx, name.c_str());
      if (src == nullptr) {
        raise("source tensor missing while uploading: " + name);
      }
      if (dst->type == src->type) {
        ggml_backend_tensor_set(dst, src->data, 0, ggml_nbytes(src));
      } else if (dst->type == GGML_TYPE_F16 && src->type == GGML_TYPE_F32) {
        const size_t count = ggml_nelements(src);
        std::vector<ggml_fp16_t> values(count);
        ggml_fp32_to_fp16_row(
            static_cast<const float*>(src->data),
            values.data(),
            static_cast<int64_t>(count));
        ggml_backend_tensor_set(
            dst, values.data(), 0, values.size() * sizeof(ggml_fp16_t));
      } else {
        raise("unsupported tensor dtype conversion while uploading: " + name);
      }
    }

    for (const auto& [name, dst] : graph.weights) {
      if (!name.ends_with("_br")) {
        continue;
      }
      const std::string srcName =
          name.substr(0, name.size() - std::string("_br").size());
      const std::vector<float> values =
          tensorToF32(ggml_get_tensor(srcCtx, srcName.c_str()), srcName);
      ggml_backend_tensor_set(
          dst, values.data(), 0, values.size() * sizeof(float));
    }

    for (const auto& [name, dst] : graph.weights) {
      if (!name.ends_with(".scale")) {
        continue;
      }
      const std::string prefix =
          name.substr(0, name.size() - std::string(".scale").size());
      const std::vector<float> weight = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".weight").c_str()),
          prefix + ".weight");
      const std::vector<float> var = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".running_var").c_str()),
          prefix + ".running_var");
      std::vector<float> scale(weight.size());
      for (size_t i = 0; i < weight.size(); ++i) {
        scale[i] = weight[i] / std::sqrt(var[i] + kBatchNormEps);
      }
      ggml_backend_tensor_set(
          dst, scale.data(), 0, scale.size() * sizeof(float));

      // Fold this BN scale into the preceding conv's F16 weights (per output
      // channel) so applyBn only needs the shift add. The BN named "<P>.1"
      // belongs to the conv weight "<P>.0.weight"; both are already uploaded.
      if (prefix.size() >= 2 && prefix.substr(prefix.size() - 2) == ".1") {
        const std::string convWeightName =
            prefix.substr(0, prefix.size() - 2) + ".0.weight";
        auto it = graph.weights.find(convWeightName);
        if (it != graph.weights.end()) {
          struct ggml_tensor* wTensor = it->second;
          if (wTensor->type != GGML_TYPE_F16) {
            raise("BN fold: expected F16 conv weight for " + convWeightName);
          }
          const int64_t oc = wTensor->ne[3];
          if (static_cast<size_t>(oc) != scale.size()) {
            raise("BN fold: output-channel mismatch for " + convWeightName);
          }
          const int64_t perOc = wTensor->ne[0] * wTensor->ne[1] * wTensor->ne[2];
          const size_t n = static_cast<size_t>(oc * perOc);
          std::vector<ggml_fp16_t> wbuf(n);
          ggml_backend_tensor_get(
              wTensor, wbuf.data(), 0, n * sizeof(ggml_fp16_t));
          for (int64_t o = 0; o < oc; ++o) {
            const float s = scale[static_cast<size_t>(o)];
            for (int64_t i = 0; i < perOc; ++i) {
              const size_t idx = static_cast<size_t>((o * perOc) + i);
              wbuf[idx] =
                  ggml_fp32_to_fp16(ggml_fp16_to_fp32(wbuf[idx]) * s);
            }
          }
          ggml_backend_tensor_set(
              wTensor, wbuf.data(), 0, n * sizeof(ggml_fp16_t));
        }
      }
    }

    for (const auto& [name, dst] : graph.weights) {
      if (!name.ends_with(".shift")) {
        continue;
      }
      const std::string prefix =
          name.substr(0, name.size() - std::string(".shift").size());
      const std::vector<float> bias = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".bias").c_str()),
          prefix + ".bias");
      const std::vector<float> mean = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".running_mean").c_str()),
          prefix + ".running_mean");
      const std::vector<float> weight = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".weight").c_str()),
          prefix + ".weight");
      const std::vector<float> var = tensorToF32(
          ggml_get_tensor(srcCtx, (prefix + ".running_var").c_str()),
          prefix + ".running_var");
      std::vector<float> shift(bias.size());
      for (size_t i = 0; i < bias.size(); ++i) {
        const float scale = weight[i] / std::sqrt(var[i] + kBatchNormEps);
        shift[i] = bias[i] - (mean[i] * scale);
      }
      ggml_backend_tensor_set(
          dst, shift.data(), 0, shift.size() * sizeof(float));
    }
  }

  void loadRecurrentWeights(struct ggml_context* srcCtx) {
    auto loadTensor = [&](const std::string& name) {
      return tensorToF32(ggml_get_tensor(srcCtx, name.c_str()), name);
    };

    for (int layer = 0; layer < kLstmLayerCount; ++layer) {
      for (int direction = 0; direction < kLstmDirectionCount; ++direction) {
        const std::string suffix = direction == 0 ? "" : "_reverse";
        const std::string base = "crnn.decoder.";
        LstmWeights& dst =
            lstm[static_cast<size_t>(layer)][static_cast<size_t>(direction)];
        const std::string layerStr = std::to_string(layer);
        std::string weightIhPath = base;
        weightIhPath.append("weight_ih_l").append(layerStr).append(suffix);
        std::string weightHhPath = base;
        weightHhPath.append("weight_hh_l").append(layerStr).append(suffix);
        std::string biasIhPath = base;
        biasIhPath.append("bias_ih_l").append(layerStr).append(suffix);
        std::string biasHhPath = base;
        biasHhPath.append("bias_hh_l").append(layerStr).append(suffix);
        dst.weightIh = loadTensor(weightIhPath);
        dst.weightHh = loadTensor(weightHhPath);
        dst.biasIh = loadTensor(biasIhPath);
        dst.biasHh = loadTensor(biasHhPath);
        dst.inputSize = layer == 0 ? kFeatureChannels
                                   : kLstmHiddenSize * kLstmDirectionCount;
      }
    }
    linearWeight = loadTensor("crnn.linear.weight");
    linearBias = loadTensor("crnn.linear.bias");
  }

  void buildGraph() {
    graph.graphCtx = std::unique_ptr<struct ggml_context, decltype(&ggml_free)>(
        ggml_init(
            {.mem_size = (ggml_tensor_overhead() * kGraphCtxTensorOverhead) +
                         ggml_graph_overhead(),
             .mem_buffer = nullptr,
             .no_alloc = true}),
        ggml_free);
    if (!graph.graphCtx) {
      raise("failed to allocate GGML graph context");
    }

    struct ggml_context* ctx = graph.graphCtx.get();
    graph.input = ggml_new_tensor_4d(
        ctx,
        GGML_TYPE_F32,
        RECOG_WIDTH,
        RECOG_HEIGHT,
        kInputChannels,
        batchSize);
    ggml_set_name(graph.input, "recognition_input");
    ggml_set_input(graph.input);

    GraphBuilder gb{.ctx = ctx, .w = graph.weights};
    struct ggml_tensor* x = gb.convBnAct(
        graph.input,
        "crnn.features.0.0",
        "crnn.features.0.1",
        2,
        2,
        3,
        true,
        true);
    for (const BlockConfig& cfg : kBlocks) {
      x = gb.invertedResidual(x, cfg);
    }
    x = gb.convBnAct(
        x, "crnn.features.12.0", "crnn.features.12.1", 1, 1, 1, true, true);
    graph.features = x;
    ggml_set_name(graph.features, "crnn_features");
    ggml_set_output(graph.features);

    graph.graph = ggml_new_graph_custom(ctx, kMaxGraphNodes, /*grads=*/false);
    ggml_build_forward_expand(graph.graph, graph.features);

    graph.graphBuffer =
        ggml_backend_alloc_ctx_tensors(graph.graphCtx.get(), graph.backend);
    if (graph.graphBuffer == nullptr) {
      raise("failed to allocate GGML recognition graph buffer");
    }
  }
};

