// UTF-8 lead-byte masks for parseVocabToChars: a UTF-8 leading byte
// determines the length of the encoded code point by the position of the
// first 0 bit. See https://en.wikipedia.org/wiki/UTF-8#Encoding for the bit
// patterns. These masks isolate that prefix so the test `(c & mask) == val`
// classifies the byte.
constexpr unsigned char kUtf8OneByteMask = 0x80;    // 1000 0000
constexpr unsigned char kUtf8TwoByteMask = 0xE0;    // 1110 0000
constexpr unsigned char kUtf8TwoByteValue = 0xC0;   // 1100 0000
constexpr unsigned char kUtf8ThreeByteMask = 0xF0;  // 1111 0000
constexpr unsigned char kUtf8ThreeByteValue = 0xE0; // 1110 0000
constexpr unsigned char kUtf8FourByteMask = 0xF8;   // 1111 1000
constexpr unsigned char kUtf8FourByteValue = 0xF0;  // 1111 0000

std::vector<std::string> parseVocabToChars(const std::string& vocab) {
  std::vector<std::string> chars;
  size_t i = 0;
  while (i < vocab.size()) {
    int len = 1;
    const auto c = static_cast<unsigned char>(vocab[i]);
    if ((c & kUtf8OneByteMask) == 0) {
      len = 1;
    } else if ((c & kUtf8TwoByteMask) == kUtf8TwoByteValue) {
      len = 2;
    } else if ((c & kUtf8ThreeByteMask) == kUtf8ThreeByteValue) {
      len = 3;
    } else if ((c & kUtf8FourByteMask) == kUtf8FourByteValue) {
      len = 4;
    }
    chars.push_back(vocab.substr(i, static_cast<size_t>(len)));
    i += static_cast<size_t>(len);
  }
  return chars;
}

